#ifndef __EXAMPLE_HH__
#define __EXAMPLE_HH__

#include <memory>
#include <string>
#include <vector>

#include "feature.hh"

namespace veedubparse
{
  struct GeneralExample
    {
      char*                                     label;
      char*                                     importance;
      char*                                     tag;
      std::vector<Feature>                      f[256];
      std::shared_ptr<std::string>              buf;

      void
      reset ()
        {
          label = importance = tag = 0;
          for (unsigned int n = 0; n < 256; ++n) { f[n].clear (); }
          buf->clear ();
        }
    };
}

#endif // __EXAMPLE_HH__
